"""""
Q1. Create one variable containing following type of data:
(i) string
(ii) list
(iii) float
(iv) tuple   
"""""

# String
str = "Ajay Rajput"
print(str)
print("type is ",type(str))

#list
lst= [1,2,3,4,56,"ajay","vijay"]
print(lst)
print("type is",type(lst))

# float
f = 50.555
print(f)
print("type is",type(f))

# tuple
t = (1,2,3,4,5,6,"ajay","Rajput")
print(t)
print("type is ",type(t))